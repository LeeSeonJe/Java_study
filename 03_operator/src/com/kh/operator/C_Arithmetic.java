package com.kh.operator;

public class C_Arithmetic {
	public void method1() {
		int num1 = 10;
		int num2 = 3;

		System.out.println("num1 + num2 = " + (num1 + num2)); // 13
		System.out.println("num1 - num2 = " + (num1 - num2)); // 7
		System.out.println("num1 * num2 = " + (num1 * num2)); // 30
		System.out.println("num1 / num2 = " + (num1 / num2)); // 3
		System.out.println("num1 % num2 = " + (num1 % num2)); // 1
	}

	public void methdo2() {
		double num1 = 35.0;
		double num2 = 10.0;

		System.out.println("num1 + num2 = " + (num1 + num2)); // 45.0
		System.out.println("num1 - num2 = " + (num1 - num2)); // 25.0
		System.out.println("num1 * num2 = " + (num1 * num2)); // 350.0
		System.out.println("num1 / num2 = " + (num1 / num2)); // 3.5
		System.out.println("num1 % num2 = " + (num1 % num2)); // 5.0
	}
}